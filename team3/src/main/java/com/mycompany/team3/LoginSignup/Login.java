    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
          //System.out.println("Sign Up");
                String Email, Password, query, PhoneNo, passDb = null, JobID = null;
                int notFound = 0;
                
                
               
                String SUrl, SUser, SPass;
                
                SUrl = "jdbc:MySQL://localhost:3306/java_user_database";
                SUser = "root";
                SPass = "";
                
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
                    Statement st = con.createStatement();
                    
                    
                    //Seperate the email and phone number is vaild or not
                    String text = emailtoPhone.getText();
                    char firstChar = text.charAt(0);
                   
                    if("".equals(emailtoPhone.getText())){
                        
                       JOptionPane.showMessageDialog(new JFrame(), "Invalid input. Must start with a letter (email) or number (phone)", "Error", 
                               JOptionPane.ERROR_MESSAGE );
                        }
                    
                    else if ((Character.isLetter(firstChar)) && (!text.endsWith("@gmail.com") ))  {
                                                                                                     
                                JOptionPane.showMessageDialog(new JFrame(), "Enter a valid Gmail address", "Error", 
                                          JOptionPane.ERROR_MESSAGE);
                    } 
                    
                 else if ((Character.isDigit(firstChar)) && (text.length() != 10)) {
                     
                           
                                JOptionPane.showMessageDialog(new JFrame(), "Phone number must be exactly 10 digits", "Error", 
                                          JOptionPane.ERROR_MESSAGE);
                    }
                 
                else if(("".equals(password.getText())) || (password.getText().length() !=6)){
                        
                       JOptionPane.showMessageDialog(new JFrame(), "Must be Enter the password with minimum 6 charactors", "Error", 
                               JOptionPane.ERROR_MESSAGE );
                        }
                         
                 else {      
                         Email = emailtoPhone.getText();
                         PhoneNo = emailtoPhone.getText();
                         Password = password.getText();
                     
                         
                        
                         
                      query = "SELECT * FROM user WHERE email= '"+Email+"' OR Phone_number= '"+PhoneNo+"'";
       
            ResultSet rs = st.executeQuery(query);
            while(rs.next()){
                passDb = rs.getString("password");
                JobID = rs.getString("Occupation");
                notFound = 1;
            }
            if(notFound == 1 && Password.equals(passDb)){
            
                    switch(JobID){
                            case "Patient":
                                    Patient_Home PatientFrame = new Patient_Home();
                                    PatientFrame.setVisible(true);
                                    PatientFrame.pack();
                                    PatientFrame.setLocationRelativeTo(null); 
                                    this.dispose();
                                    break;
                
                            case "Doctor":
                                    Doctor_Home DoctorFrame = new Doctor_Home();
                                    DoctorFrame.setVisible(true);
                                    DoctorFrame.pack();
                                    DoctorFrame.setLocationRelativeTo(null); 
                                    this.dispose();
                                    break;
                
                            case "Pharmacist":
                                    Pharmacist_Home PharmacistFrame = new Pharmacist_Home();
                                    PharmacistFrame.setVisible(true);
                                    PharmacistFrame.pack();
                                    PharmacistFrame.setLocationRelativeTo(null); 
                                    this.dispose();
                                    break;
                
                            case "Admin":
                                    Admin_Home AdminFrame = new Admin_Home();
                                    AdminFrame.setVisible(true);
                                    AdminFrame.pack();
                                    AdminFrame.setLocationRelativeTo(null); 
                                    this.dispose();
                                    break;
                                    
                            default :
                                System.out.println("Invalid role");
                                }
                    
                emailtoPhone.setText("");    
                password.setText("");
                
            }else{
               JOptionPane.showMessageDialog(new JFrame(), "Incorrect email or password", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
           
            
            } 
                }catch(Exception e){
           System.out.println("Error!" + e.getMessage()); 
        }
                
     
    }//GEN-LAST:event_LoginBtnActionPerformed

